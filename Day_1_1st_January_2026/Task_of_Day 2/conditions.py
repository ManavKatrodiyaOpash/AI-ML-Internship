# if, if-else, nested if-else, elif statements in Python

# Example of if and if-else statement
age = 18

if age >= 18:
    print("You are eligible to drive the car.")
else:
    print("You are not eligible to drive the car.")
    
# Example of nested if-else statement
marks = 85
if marks >= 90:
    print("Grade: A")
elif marks >= 75:
    print("Grade: B")
elif marks >= 60:
    print("Grade: C")
elif marks >= 40:
    print("Grade: D")
else:
    print("Grade: F")